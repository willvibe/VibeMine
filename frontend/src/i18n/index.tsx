type Lang = keyof typeof translations;
type TranslationKey = keyof typeof translations.zh;

interface I18nContextType {
  lang: Lang;
  t: (key: TranslationKey, params?: Record<string, string | number>) => string;
  setLang: (lang: Lang) => void;
}

const I18nContext = createContext<I18nContextType>({
  lang: 'zh',
  t: () => '',
  setLang: () => {},
});

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => {
    const saved = localStorage.getItem('vibemine_lang');
    return (saved === 'en' || saved === 'zh') ? saved : 'zh';
  });

  useEffect(() => {
    localStorage.setItem('vibemine_lang', lang);
  }, [lang]);

  const t = useCallback((key: TranslationKey, params?: Record<string, string | number>): string => {
    let text = translations[lang][key] || translations.zh[key] || key;
    if (params) {
      Object.entries(params).forEach(([k, v]) => {
        text = text.replaceAll(`{${k}}`, String(v));
      });
    }
    return text;
  }, [lang]);

  const setLang = useCallback((newLang: Lang) => setLangState(newLang), []);

  const contextValue = useMemo(() => ({ lang, t, setLang }), [lang, t, setLang]);

  return (
    <I18nContext.Provider value={contextValue}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}

